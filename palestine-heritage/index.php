<script>
// Fetch and display live statistics
async function loadStatistics() {
    try {
        const response = await fetch('api/get-statistics.php');
        const result = await response.json();
        
        if (result.success) {
            const data = result.data;
            
            // Animate counters
            animateValue('totalSupporters', 0, data.total_supporters, 2000);
            document.getElementById('totalDonations').textContent = data.total_donations;
            animateValue('recentSupporters', 0, data.recent_supporters, 2000);
            animateValue('newsletterSubs', 0, data.newsletter_subscribers, 2000);
        }
    } catch (error) {
        console.error('Error loading statistics:', error);
    }
}

// Animate number counting
function animateValue(id, start, end, duration) {
    const element = document.getElementById(id);
    const range = end - start;
    const increment = range / (duration / 16);
    let current = start;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= end) {
            current = end;
            clearInterval(timer);
        }
        element.textContent = Math.floor(current);
    }, 16);
}

// Load statistics on page load
document.addEventListener('DOMContentLoaded', loadStatistics);
</script>
